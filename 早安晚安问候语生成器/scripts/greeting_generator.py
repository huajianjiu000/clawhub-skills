    selected = random.choice(greetings)
    
    if custom_keyword:
        result = selected.replace("～", f"～{custom_keyword}")
    else:
        result = selected
    
    return {
        "type": greeting_type,
        "style": style,
        "greeting": result,
        "alternatives": random.sample(greetings, min(3, len(greetings)))
    }

